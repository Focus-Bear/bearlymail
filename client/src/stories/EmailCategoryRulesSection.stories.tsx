import i18n from 'i18n';

import { ContextSection } from 'components/settings/guide-ai/ContextSection';
import { CONTEXT_SOURCE_AUTOGENERATED, CONTEXT_SOURCE_USER_EDITED } from 'constants/strings';

const meta: Meta<typeof ContextSection> = {
  title: 'Settings/GuideAI/ContextEmailCategories',
  component: ContextSection,
  parameters: {
    layout: 'padded',
    docs: {
      description: {
        component:
          'Learned email category names under Guide our AI (context). Deterministic matching rules are documented under Settings/CategoryRules/DeterministicRules.',
      },
    },
  },
};

export default meta;
type Story = StoryObj<typeof ContextSection>;

type UserContextRow = {
  contextId: string;
  contextKey: string;
  contextValue: string;
  source: string;
  explanation?: string;
};

