const Wrapper: React.FC<{ children: React.ReactNode }> = ({ children }) => (
  <I18nextProvider i18n={i18n}>
    <div style={{ maxWidth: 720, margin: '0 auto' }}>{children}</div>
  </I18nextProvider>
);

const InteractiveEmailCategorySection: React.FC<{
  initialContexts: UserContextRow[];
  initialEditingId?: string | null;
  initialAdding?: boolean;
  isInitiallyExpanded?: boolean;
}> = ({ initialContexts, initialEditingId = null, initialAdding = false, isInitiallyExpanded = true }) => {
  const { t } = useTranslation();
  const [contexts, setContexts] = useState<UserContextRow[]>(initialContexts);
  const [addingContextType, setAddingContextType] = useState<string | null>(initialAdding ? 'EMAIL_CATEGORY' : null);
  const [editingContextId, setEditingContextId] = useState<string | null>(initialEditingId);
  const [editContextValue, setEditContextValue] = useState(() => {
    if (initialEditingId) {
      return initialContexts.find(row => row.contextId === initialEditingId)?.contextValue ?? '';
    }
    return '';
  });
  const [newContextValue, setNewContextValue] = useState('');

  const onAddContext = async () => {
    const trimmed = newContextValue.trim();
    if (!trimmed) {
      return;
    }
    setContexts(prev => [
      ...prev,
      {
        contextId: `new-${Date.now()}`,
        contextKey: 'EMAIL_CATEGORY',
        contextValue: trimmed,
        source: CONTEXT_SOURCE_USER_EDITED,
        explanation: '',
      },
    ]);
    setNewContextValue('');
    setAddingContextType(null);
  };

  const onUpdateContext = async () => {
    const trimmed = editContextValue.trim();
    if (!trimmed || !editingContextId) {
      return;
    }
    setContexts(prev =>
      prev.map(row =>
        row.contextId === editingContextId ? { ...row, contextValue: trimmed, source: CONTEXT_SOURCE_USER_EDITED } : row
      )
    );
    setEditingContextId(null);
    setEditContextValue('');
  };

  const onDeleteContext = (contextId: string) => {
    setContexts(prev => prev.filter(row => row.contextId !== contextId));
    if (editingContextId === contextId) {
      setEditingContextId(null);
      setEditContextValue('');
    }
  };

  return (
    <ContextSection
      title={t('settings.contextSections.emailCategories')}
      contextKey="EMAIL_CATEGORY"
      contexts={contexts}
      addLabel={t('settings.addContext.emailCategories')}
      tooltipContent={t('settings.contextTypes.tooltip.emailCategories')}
      addingContextType={addingContextType}
      editingContextId={editingContextId}
      editContextValue={editContextValue}
      newContextValue={newContextValue}
      onAddContext={onAddContext}
      onUpdateContext={onUpdateContext}
      onDeleteContext={onDeleteContext}
      onNewContextValueChange={setNewContextValue}
      onAddingContextTypeChange={setAddingContextType}
      onEditingContextIdChange={setEditingContextId}
      onEditContextValueChange={setEditContextValue}
      isInitiallyExpanded={isInitiallyExpanded}
    />
  );
};

