/**
 * Validates a category consolidation response
 * @param {string|object} output - The LLM output
 * @param {object} context - Test context with expected values
 * @returns {boolean} - true if valid
 */
module.exports = (output, context) => {
  // Validate JSON first - handle markdown code blocks
  let parsed;
  try {
    let jsonString = typeof output === 'string' ? output : JSON.stringify(output);
    // Remove markdown code blocks if present
    jsonString = jsonString.replace(/^```json\s*/i, '').replace(/^```\s*/i, '').replace(/\s*```$/i, '').trim();
    // Extract JSON array if wrapped in other text
    const jsonMatch = jsonString.match(/\[[\s\S]*\]/);
    if (jsonMatch) {
      jsonString = jsonMatch[0];
    }
    parsed = JSON.parse(jsonString);
  } catch (e) {
    throw new Error('Response must be valid JSON array. Got: ' + (typeof output === 'string' ? output.substring(0, 200) : JSON.stringify(output).substring(0, 200)));
  }
  
  if (!Array.isArray(parsed)) {
    throw new Error('Response must be a JSON array of categories');
  }
  
  // Validate minimum count if specified
  const minCount = context.config?.minCount;
  if (minCount !== undefined && parsed.length < minCount) {
    throw new Error(`Expected at least ${minCount} categories, got ${parsed.length}`);
  }
  
  // Validate maximum count if specified
  const maxCount = context.config?.maxCount;
  if (maxCount !== undefined && parsed.length > maxCount) {
    throw new Error(`Expected at most ${maxCount} categories, got ${parsed.length}`);
  }
  
  // Validate each category has required fields
  const allNames = [];
  const allDescriptions = [];
  const userAddedCategories = [];
  const autoGeneratedCategories = [];
  
  for (let i = 0; i < parsed.length; i++) {
    const item = parsed[i];
    if (!item.name || typeof item.name !== 'string') {
      throw new Error(`Category ${i + 1} must have a "name" string`);
    }
    if (!item.description || typeof item.description !== 'string') {
      throw new Error(`Category ${i + 1} must have a "description" string`);
    }
    if (typeof item.isUserAdded !== 'boolean') {
      throw new Error(`Category ${i + 1} must have an "isUserAdded" boolean`);
    }
    
    allNames.push(item.name.toLowerCase());
    allDescriptions.push(item.description.toLowerCase());
    
    if (item.isUserAdded) {
      userAddedCategories.push(item.name.toLowerCase());
    } else {
      autoGeneratedCategories.push(item.name.toLowerCase());
    }
  }
  
  // Check that user-added categories are preserved (shouldPreserveUserCategories)
  const shouldPreserveUserCategories = context.config?.shouldPreserveUserCategories;
  if (shouldPreserveUserCategories && Array.isArray(shouldPreserveUserCategories)) {
    for (const userCat of shouldPreserveUserCategories) {
      const found = userAddedCategories.some(name => name.includes(userCat.toLowerCase()));
      if (!found) {
        throw new Error(`User-added category "${userCat}" should be preserved but was not found. Found user categories: ${userAddedCategories.join(', ')}`);
      }
    }
  }
  
  // Check for required category names (shouldContainCategory)
  const shouldContainCategory = context.config?.shouldContainCategory;
  if (shouldContainCategory && Array.isArray(shouldContainCategory)) {
    for (const cat of shouldContainCategory) {
      const found = allNames.some(name => name.includes(cat.toLowerCase()));
      if (!found) {
        throw new Error(`Expected to find category containing "${cat}", but didn't. Found: ${allNames.join(', ')}`);
      }
    }
  }
  
  // Check that certain categories were merged (shouldNotContainCategory)
  const shouldNotContainCategory = context.config?.shouldNotContainCategory;
  if (shouldNotContainCategory && Array.isArray(shouldNotContainCategory)) {
    for (const cat of shouldNotContainCategory) {
      const found = allNames.some(name => name.includes(cat.toLowerCase()));
      if (found) {
        throw new Error(`Category "${cat}" should have been merged/removed but was found. Found: ${allNames.join(', ')}`);
      }
    }
  }
  
  // Check that descriptions contain certain keywords (shouldContainInDescription)
  const shouldContainInDescription = context.config?.shouldContainInDescription;
  if (shouldContainInDescription && Array.isArray(shouldContainInDescription)) {
    for (const keyword of shouldContainInDescription) {
      const found = allDescriptions.some(desc => desc.includes(keyword.toLowerCase()));
      if (!found) {
        throw new Error(`Expected to find "${keyword}" in some category description, but didn't. Found descriptions: ${allDescriptions.join(' | ')}`);
      }
    }
  }
  
  // Check that consolidated count is less than original (shouldReduceCount)
  const originalCount = context.config?.originalCount;
  if (originalCount !== undefined && parsed.length >= originalCount) {
    throw new Error(`Expected consolidation to reduce category count from ${originalCount}, but got ${parsed.length}`);
  }
  
  return true;
};
