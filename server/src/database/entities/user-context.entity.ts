import {
  Column,
  CreateDateColumn,
  Entity,
  Index,
  JoinColumn,
  ManyToOne,
  PrimaryGeneratedColumn,
  UpdateDateColumn,
} from "typeorm";

import {
  makeEncryptedColumnTransformer,
  makeEncryptedJsonTransformer,
} from "../../encryption/encryption.helper";
import { User } from "./user.entity";

export enum ContextKey {
  // Core context categories
  // Important contacts to prioritize
  VIP_CONTACT = "VIP_CONTACT",
  // User's goals for prioritization
  MY_GOALS = "MY_GOALS",
  // Things user doesn't care about (deprioritize)
  DONT_CARE = "DONT_CARE",
  // Current projects/topics with priority (1-3)
  WORKING_ON = "WORKING_ON",
  // Info about the user for drafting
  USER_INFO = "USER_INFO",
  // What the user considers urgent
  URGENT = "URGENT",
  // What the user doesn't consider important
  NOT_IMPORTANT = "NOT_IMPORTANT",
  // Common questions and answers from user replies
  Q_AND_A = "Q_AND_A",
  // Email categories for grouping (e.g., Newsletters, Customer Support)
  EMAIL_CATEGORY = "EMAIL_CATEGORY",

  // Legacy/other
  // Kept for backward compatibility
  PROJECT_NAME = "PROJECT_NAME",
  // Kept for backward compatibility
  COLLEAGUE_NAME = "COLLEAGUE_NAME",
  // Kept for backward compatibility
  CURRENT_TOPIC = "CURRENT_TOPIC",
  WRITING_STYLE_TONE = "WRITING_STYLE_TONE",
  COMMON_PHRASE = "COMMON_PHRASE",
  AVERAGE_REPLY_TIME = "AVERAGE_REPLY_TIME",
  OTHER = "OTHER",
}

export enum Source {
  AUTOGENERATED = "AUTOGENERATED",
  USER_EDITED = "USER_EDITED",
  UNAPPROVED = "UNAPPROVED",
}

@Entity("user_contexts")
// Critical index for getUserContext queries
@Index(["userId"])
// For filtering by context type
@Index(["userId", "contextKey"])
export class UserContext {
  @PrimaryGeneratedColumn("uuid")
  contextId: string;

  @Column()
  userId: string;

  @Column({
    type: "enum",
    enum: ContextKey,
  })
  contextKey: ContextKey;

  @Column("text", {
    transformer: makeEncryptedColumnTransformer("user_contexts.contextValue"),
  })
  contextValue: string;

  /**
   * Stable snake_case identifier for EMAIL_CATEGORY rows only.
   * Used in LLM shortlist / priority prompts so models return ids instead of paraphrased names.
   */
  @Column({
    type: "varchar",
    length: 128,
    nullable: true,
  })
  categoryKey: string | null;

  /**
   * Family this category belongs to ({@link CategoryFamily}). Only meaningful on
   * EMAIL_CATEGORY rows. Null means unassigned — the category shows under the
   * "Other / Uncategorised" family until it is assigned (by the taxonomy seeder
   * or the user).
   */
  @Column({
    type: "uuid",
    nullable: true,
  })
  familyId: string | null;

  @Column({
    type: "int",
    nullable: true,
    comment:
      "1 = top priority, 2 = medium, 3 = low (used for WORKING_ON items)",
  })
  priority: number;

  @Column({
    type: "enum",
    enum: Source,
    default: Source.AUTOGENERATED,
  })
  source: Source;

  @Column("text", {
    nullable: true,
    transformer: makeEncryptedColumnTransformer("user_contexts.explanation"),
    comment: "Explanation/rationale for why this context was identified",
  })
  explanation: string;

  @Column("simple-array", {
    nullable: true,
    comment: "Thread IDs that contributed to this context (for fact-checking)",
  })
  sourceThreadIds: string[];

  @CreateDateColumn()
  createdAt: Date;

  @UpdateDateColumn()
  lastModified: Date;

  @Column({
    type: "boolean",
    default: false,
    comment:
      "Flag set by migration 1786000000000 — true means this EMAIL_CATEGORY row " +
      "needs dedup processing (decrypt, merge duplicates, keep oldest UUID).",
  })
  needsCategoryDedup: boolean;

  /**
   * Alternate spellings / LLM-generated variants of this EMAIL_CATEGORY name.
   * When the LLM suggests a near-duplicate that Levenshtein + AI confirm as the
   * same category, the misspelling is stored here so future lookups skip the LLM.
   * Only meaningful on EMAIL_CATEGORY rows.
   */
  @Column("text", {
    nullable: true,
    transformer: makeEncryptedJsonTransformer("user_contexts.alternateNames"),
    comment:
      "Alternate names/misspellings for EMAIL_CATEGORY rows — enables fuzzy " +
      "dedup without repeated LLM calls (issue #2065). Stored as encrypted " +
      "JSON so names with commas survive round-trip and stay encrypted at rest.",
  })
  alternateNames: string[] | null;

  @ManyToOne(() => User, (user) => user.contexts, { onDelete: "CASCADE" })
  @JoinColumn({ name: "userId" })
  user: User;
}
