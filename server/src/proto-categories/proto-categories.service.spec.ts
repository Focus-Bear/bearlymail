      userContextRepo.find.mockResolvedValue([]);
      userContextRepo.create.mockImplementation((ctx: unknown) => ctx);
      userContextRepo.save.mockResolvedValue({ contextId: "ctx-new" });
      protoCategoryRepo.update.mockResolvedValue({});
