function makeUserContext(
  contextId: string,
  contextValue: string,
  alternateNames: string[] | null = null,
): UserContext {
  const ctx = new UserContext();
  ctx.contextId = contextId;
  ctx.userId = "user-1";
  ctx.contextKey = ContextKey.EMAIL_CATEGORY;
  ctx.contextValue = contextValue;
  ctx.alternateNames = alternateNames;
  ctx.source = Source.AUTOGENERATED;
  return ctx;
}

